// ==================== 获取缩略图 ====================
function getThumb(path) {
  if (!path) return '';
  // 将 images/xxx/photo.jpg 转为 thumbs/xxx/photo.webp
  let thumb = path
    .replace(/^images\//, '')
    .replace(/_看图王\.jpg$/i, '.webp')
    .replace(/\.jpg$/i, '.webp')
    .replace(/\.jpeg$/i, '.webp')
    .replace(/\.png$/i, '.webp');
  return `${CDN.thumbs}/${thumb}?v=20260714-3`;
}

// 获取完整大图（灯箱查看用原图）
function getFull(path) {
  if (!path) return '';
  if (path.startsWith('http')) return path;
  return path + '?v=20260714-3';
}

// 格式化文章内容，散文分段落，诗保持分行居中
function formatBody(text) {
  if (!text) return '';
  // 统一换行符
  var normalized = text.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
  // 检测是否为诗：无空行、多行（2-20行）、总长度短、每行短
  var lines = normalized.split('\n');
  var nonEmptyLines = [];
  var hasEmptyLine = false;
  for (var i = 0; i < lines.length; i++) {
    if (lines[i].trim()) {
      nonEmptyLines.push(lines[i].trim());
    } else {
      hasEmptyLine = true;
    }
  }
  var avgLen = nonEmptyLines.reduce(function(s, l){ return s + l.length; }, 0) / nonEmptyLines.length;
  if (!hasEmptyLine && nonEmptyLines.length >= 2 && nonEmptyLines.length <= 20 && normalized.length < 500 && avgLen < 30) {
    return '<div class="poem-lines">' + nonEmptyLines.join('<br>') + '</div>';
  }
  // 散文：按换行分段（每行即一段）
  var paras = normalized.split(/\n+/);
  var html = '';
  for (var i = 0; i < paras.length; i++) {
    var p = paras[i].trim();
    if (p) html += '<p>' + p.replace(/\n/g, '<br>') + '</p>';
  }
  return html;
}

