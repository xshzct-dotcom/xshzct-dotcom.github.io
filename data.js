// ==================== 智能预加载 ====================
class Preloader {
  constructor() { this.cache = new Set(); }
  load(url) {
    if (this.cache.has(url)) return;
    this.cache.add(url);
    const img = new Image();
    img.src = url;
  }
  preloadAdjacent(current, photos, count = 2) {
    for (let i = 1; i <= count; i++) {
      const next = photos[(current + i) % photos.length];
      const prev = photos[(current - i + photos.length) % photos.length];
      this.load(getFull(next));
      this.load(getFull(prev));
    }
  }

}
